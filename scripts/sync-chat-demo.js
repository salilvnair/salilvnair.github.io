#!/usr/bin/env node
/**
 * Builds convengine-chat-demo as a static export and syncs it into
 * public/framework/convengine-chat/, then injects the client-side API mock script tag.
 *
 * Run from the salilvnair.github.io root:
 *   npm run sync-chat-demo
 */

import { execSync } from 'child_process';
import { readFileSync, writeFileSync, cpSync, existsSync, unlinkSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname   = dirname(fileURLToPath(import.meta.url));
const ROOT        = resolve(__dirname, '..');
const CHAT_DEMO   = resolve(__dirname, '../../convengine-ui-builder/convengine-chat-demo');
const DEST        = resolve(ROOT, 'public/framework/convengine-chat');
const SOURCE_FAKE_CHAT = resolve(CHAT_DEMO, 'app/data/fake-chat.json');
const ROOT_NEXT_CONFIG = resolve(CHAT_DEMO, 'next.config.mjs');
const LOCAL_ONLY_NEXT_CONFIG = resolve(CHAT_DEMO, 'local_only/next.config.mjs');
const hadOriginalRootConfig = existsSync(ROOT_NEXT_CONFIG);

function buildClientApiMock(data) {
  const embeddedData = JSON.stringify(data, null, 2);
  return `// Auto-generated by scripts/sync-chat-demo.js
// Source: convengine-chat-demo/app/data/fake-chat.json
(function () {
  var DATA = ${embeddedData};

  function normalise(str) {
    return String(str || '')
      .toLowerCase()
      .replace(/[^a-z0-9\\s]/g, '')
      .replace(/\\s+/g, ' ')
      .trim();
  }

  var STOP_WORDS = {
    a:1,an:1,the:1,is:1,it:1,in:1,on:1,at:1,to:1,for:1,of:1,and:1,or:1,but:1,
    i:1,me:1,my:1,we:1,you:1,your:1,can:1,do:1,does:1,did:1,be:1,am:1,are:1,
    was:1,were:1,have:1,has:1,had:1,will:1,would:1,should:1,could:1,may:1,
    might:1,shall:1,let:1,please:1,hey:1,hi:1,hello:1,s:1,re:1,ll:1,ve:1,
  };

  function tokenise(str) {
    return normalise(str).split(' ').filter(function (t) { return t && !STOP_WORDS[t]; });
  }

  function jaccardSimilarity(tokA, tokB) {
    if (!tokA.length || !tokB.length) return 0;
    var setA = {}, setB = {}, union = {}, intersection = 0;
    tokA.forEach(function (t) { setA[t] = 1; union[t] = 1; });
    tokB.forEach(function (t) { setB[t] = 1; union[t] = 1; });
    tokA.forEach(function (t) { if (setB[t]) intersection++; });
    return intersection / Object.keys(union).length;
  }

  function levenshtein(a, b) {
    var m = a.length, n = b.length;
    if (m === 0) return n;
    if (n === 0) return m;
    if (a === b) return 0;
    var prev = [], i, j;
    for (i = 0; i <= n; i++) prev[i] = i;
    for (i = 1; i <= m; i++) {
      var curr = [i];
      for (j = 1; j <= n; j++) {
        var cost = a[i - 1] === b[j - 1] ? 0 : 1;
        curr[j] = Math.min(curr[j - 1] + 1, prev[j] + 1, prev[j - 1] + cost);
      }
      prev = curr;
    }
    return prev[n];
  }

  function pick(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
  }

  function matchResponse(input) {
    var text = String(input || '');
    var norm = normalise(text);
    var inTok = tokenise(text);
    var pairs = Array.isArray(DATA && DATA.pairs) ? DATA.pairs : [];
    var fallbacks = Array.isArray(DATA && DATA.fallbacks) ? DATA.fallbacks : ['Sorry, I did not understand that.'];

    // Ensure reset/forgot password prompts resolve even when phrase differs.
    if (/\b(reset|forgot|password)\b/i.test(text)) {
      for (var p = 0; p < pairs.length; p++) {
        if (/\bpassword\b/i.test(String(pairs[p].user || ''))) {
          return pairs[p].agent;
        }
      }
    }

    for (var i = 0; i < pairs.length; i++) {
      if (normalise(pairs[i].user) === norm) return pairs[i].agent;
    }

    for (var j = 0; j < pairs.length; j++) {
      var pNorm = normalise(pairs[j].user);
      if (norm.includes(pNorm) || pNorm.includes(norm)) return pairs[j].agent;
    }

    var bestPair = null;
    var bestScore = 0;
    for (var k = 0; k < pairs.length; k++) {
      var score = jaccardSimilarity(inTok, tokenise(pairs[k].user));
      if (score > bestScore) {
        bestScore = score;
        bestPair = pairs[k];
      }
    }
    if (bestPair && bestScore >= 0.35) return bestPair.agent;

    if (norm.length <= 40) {
      var bestDist = Infinity;
      var levenPair = null;
      for (var x = 0; x < pairs.length; x++) {
        var userNorm = normalise(pairs[x].user);
        if (Math.abs(userNorm.length - norm.length) > 10) continue;
        var dist = levenshtein(norm, userNorm);
        var threshold = Math.max(2, Math.floor(Math.min(norm.length, userNorm.length) * 0.30));
        if (dist < bestDist && dist <= threshold) {
          bestDist = dist;
          levenPair = pairs[x];
        }
      }
      if (levenPair) return levenPair.agent;
    }

    return pick(fallbacks);
  }

  var originalFetch = window.fetch.bind(window);
  window.fetch = async function (input, init) {
    var url = typeof input === 'string' ? input : (input && input.url) || '';
    var pathname = '';
    try {
      pathname = new URL(url, window.location.origin).pathname;
    } catch (_e) {
      pathname = '';
    }

    var isMockedPath = pathname === '/api/v1/conversation/message' || pathname.endsWith('/api/v1/conversation/message');
    var method = (init && init.method ? String(init.method) : 'GET').toUpperCase();

    if (isMockedPath && method === 'POST') {
      var body = {};
      try { body = init && init.body ? JSON.parse(init.body) : {}; } catch (_err) {}
      var payload = matchResponse(body.message || '');
      var delay = 250 + Math.random() * 500;
      await new Promise(function (r) { setTimeout(r, delay); });
      return new Response(JSON.stringify({ payload: payload }), {
        status: 200,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    return originalFetch(input, init);
  };
})();
`;
}

// ── 1. Prepare root next.config.mjs from local_only template + overrides ─────
console.log('📝  Preparing root next.config.mjs for static export...');
const originalRootConfig = hadOriginalRootConfig ? readFileSync(ROOT_NEXT_CONFIG, 'utf8') : null;
const sourceConfig = existsSync(LOCAL_ONLY_NEXT_CONFIG)
  ? readFileSync(LOCAL_ONLY_NEXT_CONFIG, 'utf8')
  : `/** @type {import('next').NextConfig} */
const nextConfig = {
  transpilePackages: ['@salilvnair/convengine-chat'],
};

export default nextConfig;
`;

const normalizedSource = sourceConfig.replace(/export\s+default\s+nextConfig\s*;?\s*$/m, '').trimEnd();
const patchedConfig = `${normalizedSource}

const __syncChatDemoExportConfig = {
  output: 'export',
  basePath: '/framework/convengine-chat',
  trailingSlash: false,
};

export default { ...nextConfig, ...__syncChatDemoExportConfig };
`;
writeFileSync(ROOT_NEXT_CONFIG, patchedConfig, 'utf8');

try {
  // ── 2. Build ───────────────────────────────────────────────────────────────
  console.log('🔨  Building convengine-chat-demo (npm run build)...');
  execSync('npm run build', { cwd: CHAT_DEMO, stdio: 'inherit' });

  const OUT = resolve(CHAT_DEMO, 'out');
  if (!existsSync(OUT)) {
    throw new Error('Build output directory "out/" not found in ' + CHAT_DEMO);
  }

  // ── 3. Copy output → public/framework/convengine-chat ────────────────────
  console.log('📂  Copying out/ → public/framework/convengine-chat/...');
  cpSync(OUT, DEST, { recursive: true });

  // ── 4. Regenerate static api-mock.js from source fake-chat.json ───────────
  console.log('🧩  Regenerating api-mock.js from app/data/fake-chat.json...');
  const fakeChatData = JSON.parse(readFileSync(SOURCE_FAKE_CHAT, 'utf8'));
  writeFileSync(resolve(DEST, 'api-mock.js'), buildClientApiMock(fakeChatData), 'utf8');

  // ── 5. Inject api-mock.js script tag ──────────────────────────────────────
  console.log('💉  Injecting api-mock.js script tag into HTML files...');
  const MOCK_TAG   = '<script src="/framework/convengine-chat/api-mock.js"></script>';
  const FIRST_CHUNK = '<script src="/framework/convengine-chat/_next/static/chunks/fd9d1056-';

  for (const file of ['index.html', 'fullscreen.html']) {
    const filePath = resolve(DEST, file);
    if (!existsSync(filePath)) {
      console.warn(`  ⚠️  ${file} not found, skipping`);
      continue;
    }
    let html = readFileSync(filePath, 'utf8');
    if (html.includes(MOCK_TAG)) {
      console.log(`  ✅  ${file} already has the mock tag`);
    } else {
      // Inject synchronously before the first async Next.js chunk
      const idx = html.indexOf(FIRST_CHUNK);
      if (idx === -1) {
        // Fallback: inject right after <head>
        html = html.replace('<head>', '<head>' + MOCK_TAG);
      } else {
        html = html.slice(0, idx) + MOCK_TAG + html.slice(idx);
      }
      writeFileSync(filePath, html, 'utf8');
      console.log(`  ✅  Injected into ${file}`);
    }
  }

  console.log('\n✨  Done! Chat demo synced to public/framework/convengine-chat/');

} finally {
  // ── 6. Restore root next.config.mjs ───────────────────────────────────────
  console.log('♻️   Restoring root next.config.mjs...');
  if (hadOriginalRootConfig) {
    writeFileSync(ROOT_NEXT_CONFIG, originalRootConfig, 'utf8');
  } else if (existsSync(ROOT_NEXT_CONFIG)) {
    unlinkSync(ROOT_NEXT_CONFIG);
  }
}
